    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText().trim();
        String password = new String(txtPassword.getPassword()).trim();

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Username dan Password tidak boleh kosong!",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Session session = null;

        try {
            session = Hibernate.getSessionFactory().openSession();

            Query<Officer> query = session.createQuery(
                    "FROM Officer WHERE username = :u",
                    Officer.class
            );
            query.setParameter("u", username);

            Officer officer = query.uniqueResult();

            if (officer == null) {
                JOptionPane.showMessageDialog(this,
                        "Username atau password salah!",
                        "Gagal Login", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (!PasswordUtil.verifyPassword(password, officer.getPassword())) {
                JOptionPane.showMessageDialog(this,
                        "Username atau password salah!",
                        "Gagal Login", JOptionPane.ERROR_MESSAGE);
                return;
            }

            JOptionPane.showMessageDialog(this,
                    "Login berhasil!\nWelcome, " + officer.getName(),
                    "Sukses", JOptionPane.INFORMATION_MESSAGE);

            new FrameMenu(officer.getRole()).setVisible(true);
            this.dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            if (session != null) session.close();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

